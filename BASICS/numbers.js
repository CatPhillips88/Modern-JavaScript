// Data Type: Numbers

// Arithmetic operators
// + : Addition
// - : Subtraction
// * : Multiplication
// ** : Exponentiation / To The Power of...
// / : Division
// % : Modulus / Remainder 
// ++ : Increment
// -- : Decrement


// With bracket order of operations
let x = 11 

let num = (x + 1) * 2;

console.log(num) // Result: 24


// Without bracket order of operations
let x = 11 

let num = x + 1 * 2;

console.log(num) // Result: 13


